package Aula02.Exercicios;

import java.util.Scanner;

public class Exercicio06 {
    public static void main(String[] args) {

        int num1 = 4;
        int num2 = 9;

        System.out.println("x > y: " + (num1 > num2));
        System.out.println("x < y: " + (num1 < num2));
        System.out.println("x >= y: " + (num1 >= num2));
        System.out.println("x <= y: " + (num1 <= num2));
        System.out.println("x == y: " + (num1 == num2));
        System.out.println("x != y: " + (num1 != num2));


    }
}
